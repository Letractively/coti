                                        <p>Assim sendo, a primeira fase do MCTI &eacute; a fase de Planejamento da Contrata&ccedil;&atilde;o de Solu&ccedil;&otilde;es de TI &ndash; PCTI. Esta fase &eacute; composta por 5 processos, 41 atividades e produz 8 artefatos. Al&eacute;m disso, 7 atores participam desta fase, que est&aacute; estruturada da seguinte forma:</p>
