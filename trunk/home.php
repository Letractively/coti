				<h2 class="title"><a href="#">Bem vindo ao CoTI -  Contrata&ccedil;&atilde;o de TI</a></h2>
				<p class="meta"><span class="date">24 Mar&ccedil;o 2011</span><span class="posted">Atualizado por <a href="#">Usu&aacute;rio</a></span></p>
				<div style="clear: both;">&nbsp;</div>
				<div class="entry">
					<p>Software desenvolvido para atender as necessidades de automatiza&ccedil;&atilde;o de contrata&ccedil;&otilde;es de TI do Instituto Federal de Alagoas.</p>
					<p>Para as contrata&ccedil;&otilde;es relacionadas a Tecnologia da Informa&ccedil;&atilde;o foi desenvolvido o Modelo de Contrata&ccedil;&atilde;o de Solu&ccedil;&otilde;es de Tecnologia da Informa&ccedil;&atilde;o &ndash; MCTI baseado nas fases e processos descritos na IN 04/2010.</p>
					<p>A IN 04/2010 em seu Art. 8&ordm;, estabelece que &ldquo;as contrata&ccedil;&otilde;es de Solu&ccedil;&otilde;es de Tecnologia da Informa&ccedil;&atilde;o dever&atilde;o seguir tr&ecirc;s fases&rdquo;, s&atilde;o elas:</p>
					<ul>
						<li>PCTI &ndash; Planejamento da Contrata&ccedil;&atilde;o de Solu&ccedil;&otilde;es de TI;</li>
						<li>SFTI &ndash; Sele&ccedil;&atilde;o do Fornecedor de Solu&ccedil;&otilde;es de TI; e</li>
						<li>GCTI &ndash; Gerenciamento do Contrato de Solu&ccedil;&atilde;o TI.</li>
					</ul>
