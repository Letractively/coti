                                            <li> PCTI-P5 &ndash; Estrat&eacute;gia da Contrata&ccedil;&atilde;o;</li>
                                            <li> PCTI-P6 &ndash; Consolidar Informa&ccedil;&otilde;es;</li>
                                            <li> Artefato: Documento de Oficializa&ccedil;&atilde;o de Demanda (DOD)</li>
